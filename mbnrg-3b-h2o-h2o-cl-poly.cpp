struct x3b_h2o_ion_v2x_p {

    x3b_h2o_ion_v2x_p();

    static const unsigned ncoeffs = h2o_ion::poly_3b_h2o_ion_v1x::size;

    double operator()(const double* w1,
                      const double* w2,
                      const double* x ,
                      double* g1,
                      double* g2,
                      double* g3) const;

public:
    double m_r3i;
    double m_r3f;

private:
    double m_kOH_intra;
    double m_kHH_intra;

    double m_kOO;
    double m_kOH;
    double m_kHH;

    double m_kXO;
    double m_kXH;

    double m_dOH_intra;
    double m_dHH_intra;

    double m_dOO;
    double m_dOH;
    double m_dHH;
    
    double m_dXO;
    double m_dXH;


private:
    double m_coeffs[ncoeffs];

private:
    double f_switch(const double& r, double& g) const;
};

//----------------------------------------------------------------------------//

double x3b_h2o_ion_v2x_p::f_switch(const double& r, double& g) const
{
    if (r > m_r3f) {
        g = 0.0;
        return 0.0;
    } else if (r > m_r3i) {
        const double t1 = M_PI/(m_r3f - m_r3i);
        const double x = (r - m_r3i)*t1;
        g = - std::sin(x)*t1/2.0;
        return (1.0 + std::cos(x))/2.0;
    } else {
        g = 0.0;
        return 1.0;
    }
}

//----------------------------------------------------------------------------//

double x3b_h2o_ion_v2x_p::operator()(const double* w1,
                             const double* w2,
                             const double* x ,
                             double* g1,
                             double* g2,
                             double* g3) const
{
    const double* Oa  = w1;
    const double* Ha1 = w1 + 3;
    const double* Ha2 = w1 + 6;

    const double* Ob  = w2;
    const double* Hb1 = w2 + 3;
    const double* Hb2 = w2 + 6;

    const double* X   = x ;

    double ksi[21];

    ksi[0] = v_intra(m_kHH_intra, m_dHH_intra, Ha1, Ha2);
    ksi[1] = v_intra(m_kHH_intra, m_dHH_intra, Hb1, Hb2);
    ksi[2] = v_intra(m_kOH_intra, m_dOH_intra,  Oa, Ha1);
    ksi[3] = v_intra(m_kOH_intra, m_dOH_intra,  Oa, Ha2);
    ksi[4] = v_intra(m_kOH_intra, m_dOH_intra,  Ob, Hb1);
    ksi[5] = v_intra(m_kOH_intra, m_dOH_intra,  Ob, Hb2);

    ksi[6] = v_main(m_kHH, m_dHH, Ha1, Hb1);
    ksi[7] = v_main(m_kHH, m_dHH, Ha1, Hb2);
    ksi[8] = v_main(m_kHH, m_dHH, Ha2, Hb1);
    ksi[9] = v_main(m_kHH, m_dHH, Ha2, Hb2);
    ksi[10] = v_main(m_kOH, m_dOH,  Oa, Hb1);
    ksi[11] = v_main(m_kOH, m_dOH,  Oa, Hb2);
    ksi[12] = v_main(m_kOH, m_dOH,  Ob, Ha1);
    ksi[13] = v_main(m_kOH, m_dOH,  Ob, Ha2);
    ksi[14] = v_main(m_kOO, m_dOO,  Oa,  Ob);
    ksi[15] = v_main(m_kXH, m_dXH,  X, Ha1);
    ksi[16] = v_main(m_kXH, m_dXH,  X, Ha2);
    ksi[17] = v_main(m_kXH, m_dXH,  X, Hb1);
    ksi[18] = v_main(m_kXH, m_dXH,  X, Hb2);
    ksi[19] = v_main(m_kXO, m_dXO,  X,  Oa);
    ksi[20] = v_main(m_kXO, m_dXO,  X,  Ob);

    double g[21];
    double retval = h2o_ion::poly_3b_h2o_ion_v1x::eval(m_coeffs, ksi, g);

    double rab[3], rac[3], rbc[3];
    double drab(0), drac(0), drbc(0);

    for (int n = 0; n < 3; ++n) {
        rab[n] = Oa[n] - Ob[n];
        drab += rab[n]*rab[n];

        rac[n] = Oa[n] - X[n];
        drac += rac[n]*rac[n];

        rbc[n] = Ob[n] - X[n];
        drbc += rbc[n]*rbc[n];
    }

    drab = std::sqrt(drab);
    drac = std::sqrt(drac);
    drbc = std::sqrt(drbc);

    double gab, gac, gbc;

    const double sab = f_switch(drab, gab);
    const double sac = f_switch(drac, gac);
    const double sbc = f_switch(drbc, gbc);

    const double s = sab*sac + sab*sbc + sac*sbc;

    for (int n = 0; n < 21; ++n)
        g[n] *= s;

    double* gOa  = g1;
    double* gHa1 = g1 + 3;
    double* gHa2 = g1 + 6;

    double* gOb  = g2;
    double* gHb1 = g2 + 3;
    double* gHb2 = g2 + 6;

    double* gX   = g3;

    g_intra(g[0], m_kHH_intra, m_dHH_intra, Ha1, Ha2, gHa1, gHa2);
    g_intra(g[1], m_kHH_intra, m_dHH_intra, Hb1, Hb2, gHb1, gHb2);
    g_intra(g[2], m_kOH_intra, m_dOH_intra,  Oa, Ha1,  gOa, gHa1);
    g_intra(g[3], m_kOH_intra, m_dOH_intra,  Oa, Ha2,  gOa, gHa2);
    g_intra(g[4], m_kOH_intra, m_dOH_intra,  Ob, Hb1,  gOb, gHb1);
    g_intra(g[5], m_kOH_intra, m_dOH_intra,  Ob, Hb2,  gOb, gHb2);

    g_main(g[6],  m_kHH, m_dHH, Ha1, Hb1, gHa1, gHb1);
    g_main(g[7],  m_kHH, m_dHH, Ha1, Hb2, gHa1, gHb2);
    g_main(g[8],  m_kHH, m_dHH, Ha2, Hb1, gHa2, gHb1);
    g_main(g[9],  m_kHH, m_dHH, Ha2, Hb2, gHa2, gHb2);
    g_main(g[10], m_kOH, m_dOH,  Oa, Hb1,  gOa, gHb1);
    g_main(g[11], m_kOH, m_dOH,  Oa, Hb2,  gOa, gHb2);
    g_main(g[12], m_kOH, m_dOH,  Ob, Ha1,  gOb, gHa1);
    g_main(g[13], m_kOH, m_dOH,  Ob, Ha2,  gOb, gHa2);
    g_main(g[14], m_kOO, m_dOO,  Oa,  Ob,  gOa,  gOb);
    g_main(g[15], m_kXH, m_dXH,  X, Ha1,  gX, gHa1);
    g_main(g[16], m_kXH, m_dXH,  X, Ha2,  gX, gHa2);
    g_main(g[17], m_kXH, m_dXH,  X, Hb1,  gX, gHb1);
    g_main(g[18], m_kXH, m_dXH,  X, Hb2,  gX, gHb2);
    g_main(g[19], m_kXO, m_dXO,  X,  Oa,  gX,  gOa);
    g_main(g[20], m_kXO, m_dXO,  X,  Ob,  gX,  gOb);

    // gradients of the switching function

    gab *= (sac + sbc)*retval/drab;
    gac *= (sab + sbc)*retval/drac;
    gbc *= (sab + sac)*retval/drbc;

    retval *= s;

    for (int n = 0; n < 3; ++n) {
        gOa[n] += gab*rab[n] + gac*rac[n];
        gOb[n] += gbc*rbc[n] - gab*rab[n];
        gX[n] -= gac*rac[n] + gbc*rbc[n];
    }

    return retval;
}

